/**
 * EmpresaReportes — Report generator for empresa admin (new feature).
 *
 * Two report types, each with their own period selector and export format choice:
 *
 *  1. Usuarios nuevos  — rows: nombre, email, teléfono, rol, tipo_cuenta,
 *                               estado, fecha_registro
 *     Source: supabase.from('users').select(...).gte/lte on created_at
 *     RLS users_empresa_admin_read scopes to empresa automatically.
 *
 *  2. Citas
 *     a) Por servicio — rows: servicio, total_citas
 *     b) Por titular  — rows: titular, citas_propias, citas_familiares, total
 *     Source: supabase.from('citas').select(paciente join) .gte/lte on fecha_hora_cita
 *     RLS citas_empresa_admin_read scopes automatically.
 *
 * Export: .xlsx  (SheetJS) / .csv  (SheetJS sheet_to_csv + UTF-8 BOM)
 */

